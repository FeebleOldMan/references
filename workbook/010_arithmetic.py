from math import log10

int_a = int(input("A: "))
int_b = int(input("B: "))
print("A+B={}".format(int_a+int_b))
print("B-A={}".format(int_b-int_a))
print("A*B={}".format(int_a*int_b))
print("A//B={}".format(int_a//int_b))
print("A%B={}".format(int_a%int_b))
print("log_10_A={}".format(log10(int_a)))
print("A**B={}".format(int_a**int_b))
