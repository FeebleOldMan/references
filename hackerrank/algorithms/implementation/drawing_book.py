def page_turns(pages, target):
    """Returns minimum number of page turns to get to target page from front or
    back.

    >>> page_turns(6, 2)
    1
    >>> page_turns(5, 4)
    0
    """
    turns = 0
    if pages != target:
        if target % 2:
            target -= 1
        turns = min(target // 2, pages // 2 - target // 2)
    return turns

